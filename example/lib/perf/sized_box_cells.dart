final fastSizePattern = RegExp(r'^[\d. ×]+$');
final characterSizes = <String, Size>{};

class FastText extends StatefulWidget {
  const FastText(this.data, {Key key}) : super(key: key);
  final String data;

  @override
  _FastTextState createState() => _FastTextState();
}

class _FastTextState extends State<FastText> {
  bool canFastSize = false;
  Size fastSize;

  @override
  void initState() {
    super.initState();
    updateCanFastSize();
  }

  @override
  void didUpdateWidget(FastText oldWidget) {
    super.didUpdateWidget(oldWidget);
    if (oldWidget.data != widget.data) {
      updateCanFastSize();
    }
  }

  void updateCanFastSize() {
    canFastSize = fastSizePattern.hasMatch(widget.data);
    fastSize = null;
  }

  void computeFastSize(TextStyle style, TextDirection direction) {
    if (characterSizes.isEmpty) {
      assert(style.fontFeatures.contains(FontFeature.tabularFigures()));
      final painter = TextPainter(
          text: TextSpan(text: '1', style: style), textDirection: direction);
      painter.layout();
      characterSizes.addEntries([
        for (int i = 0; i <= 9; i++) MapEntry('$i', painter.size),
      ]);
      characterSizes[' '] = (painter
            ..text = TextSpan(text: ' ', style: style)
            ..layout())
          .size;
      characterSizes['×'] = (painter
            ..text = TextSpan(text: '×', style: style)
            ..layout())
          .size;
      characterSizes['.'] = (painter
            ..text = TextSpan(text: '.', style: style)
            ..layout())
          .size;
    }

    fastSize = widget.data.characters.fold(Size.zero, (memo, element) {
      final charSize = characterSizes[element];
      return Size(
          memo.width + charSize.width, max(memo.height, charSize.height));
    });
  }

  @override
  Widget build(BuildContext context) {
    if (fastSize == null && canFastSize) {
      computeFastSize(
          DefaultTextStyle.of(context).style, Directionality.of(context));
    }

    final text = Text(widget.data);
    return canFastSize ? SizedBox.fromSize(size: fastSize, child: text) : text;
  }
}
